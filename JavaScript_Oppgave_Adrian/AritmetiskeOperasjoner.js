// Artimetiske Operasjoner

// + (pluss)

let a = 100;
let b = 50;
let sum1 = a + b;
console.log(sum1);

// - (minus)

let c = 100;
let d = 50;
let sum2 = c - d;
console.log(sum2);

// * (gange)

let e = 100;
let f = 50;
let sum3 = e * f;
console.log(sum3);

// / (dele)

let g = 100;
let h = 50;
let sum4 = g / h;
console.log(sum4);

// ** (opphøyd)

let i = 2;
let j = 4;
let sum5 = i ** j;
console.log(sum5);

// % (rest)

let k = 100;
let l = 50;
let sum6 = k % l;
console.log(sum6);

// ++ (øke)

let m = 100;
m++;
console.log(m);

// -- (minke)

let n = 100;
n--;
console.log(n);
