// var, let, const

// var, kan forandres

var y = "John Doe"; // Definerer variabel y som "John Doe"
var y = 0; // Definerer variabel y på nytt til 0
console.log(y); // Printer ut resultatet

// let, kan forandres

let x = "John Doe"; // Definerer variabel x som "John Doe"
x = 0; // Definerer variabel x på nytt til 0
console.log(x) // Printer ut resultatet

// const, kan ikke forandres

const PI = 3.141592653589793; // Definerer variabel PI som 3.141592653589793
PI = 3.14; // Definerer variabel PI som 3.14
PI = PI + 10; // Definerer variabel PI som PI + 10
console.log(PI); // Printer ut resultatet. men får en feil da const ikke kan defineres flere ganger